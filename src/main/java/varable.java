public class varable {
    public static void main(String[] args) {
        int a; //정수
        a = 1;
        System.out.println(a);
        a = 2;
        System.out.println(a);
        System.out.println(a+1);

        final int b = 4; //상수
        System.out.println(b);

        double c = 5.4; //실수
        System.out.println(c + 0.0321);

//        String first;
//        first = "coding";
        String first = "coding"; //문자열
        System.out.println(first);


    }
}
